    private void btnCalTotalFeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalTotalFeeActionPerformed
       float deliveryfee=Float.parseFloat(txtDeliveryFee.getText());
       int quantity=Integer.parseInt(txtQuantity.getText());
       float Unitprice=Float.parseFloat(txtUnitPrice.getText());
       float total=(quantity*Unitprice)+deliveryfee;
       lbltotal.setText("The total for the order is : "+total);
    }//GEN-LAST:event_btnCalTotalFeeActionPerformed

