    private void btnAddOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddOrderActionPerformed
        
            
            String name=txtOrderName.getText();
            int orderID=Integer.parseInt(txtCustomerOrderID.getText());
            String orderDate=txtOrderDate.getText();
            int quatity=Integer.parseInt(txtQuantity.getText());
            float unitPrice=Float.parseFloat(txtUnitPrice.getText());
            float deliveryfee=Float.parseFloat(txtDeliveryFee.getText());
            int x=selectOrderType.getSelectedIndex();
            String type=selectOrderType.getItemAt(x);
            customerObj=OrderControllerObj.addCustomerOrder(orderID, name, orderDate, quatity, unitPrice, deliveryfee, type);
            JOptionPane.showMessageDialog(rootPane, "Customer order added successfully", "Alert", 1);
        
    }//GEN-LAST:event_btnAddOrderActionPerformed

