    private void btnUpdateOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateOrderActionPerformed
        int id=Integer.parseInt(txtCustomerOrderID.getText());
        if(id==customerObj.getOrderID()){
              String name=txtOrderName.getText();
            String orderDate=txtOrderDate.getText();
            int quatity=Integer.parseInt(txtQuantity.getText());
            float unitPrice=Float.parseFloat(txtUnitPrice.getText());
            float deliveryfee=Float.parseFloat(txtDeliveryFee.getText());
            int x=selectOrderType.getSelectedIndex();
            String type=selectOrderType.getItemAt(x);
            customerObj=OrderControllerObj.updateCustomerOrder(id, name, orderDate, quatity, unitPrice, deliveryfee, type);
            JOptionPane.showMessageDialog(rootPane, "Order details updated successfully", "Alert", 1);
        }
    }//GEN-LAST:event_btnUpdateOrderActionPerformed

